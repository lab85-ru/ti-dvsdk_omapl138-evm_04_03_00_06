/******************************************************************************
 * gst_tiimgdec1_init_image
 *     Initialize or re-initializes the image stream
 ******************************************************************************/
static gboolean gst_tiimgdec1_init_image(GstTIImgdec1 *imgdec1)
{
    Rendezvous_Attrs    rzvAttrs  = Rendezvous_Attrs_DEFAULT;
    struct sched_param  schedParam;
    pthread_attr_t      attr;

    GST_LOG("Begin\n");

    /* If image has already been initialized, shut down previous decoder */
    if (imgdec1->hEngine) {
        if (!gst_tiimgdec1_exit_image(imgdec1)) {
            GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
            ("failed to shut down existing image decoder\n"), (NULL));
            return FALSE;
        }
    }

    /* Make sure we know what codec we're using */
    if (!imgdec1->engineName) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("engine name not specified\n"), (NULL));
        return FALSE;
    }

    if (!imgdec1->codecName) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("codec name not specified\n"), (NULL));
        return FALSE;
    }

    /* Initialize thread status management */
    imgdec1->threadStatus = 0UL;
    pthread_mutex_init(&imgdec1->threadStatusMutex, NULL);

    /* Initialize rendezvous objects for making threads wait on conditions */
    imgdec1->waitOnDecodeThread = Rendezvous_create(2, &rzvAttrs);
    imgdec1->waitOnDecodeDrain  = Rendezvous_create(100, &rzvAttrs);
    imgdec1->drainingEOS        = FALSE;

    /* Initialize custom thread attributes */
    if (pthread_attr_init(&attr)) {
        GST_WARNING("failed to initialize thread attrs\n");
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }

    /* Force the thread to use the system scope */
    if (pthread_attr_setscope(&attr, PTHREAD_SCOPE_SYSTEM)) {
        GST_WARNING("failed to set scope attribute\n");
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }

    /* Force the thread to use custom scheduling attributes */
    if (pthread_attr_setinheritsched(&attr, PTHREAD_EXPLICIT_SCHED)) {
        GST_WARNING("failed to set schedule inheritance attribute\n");
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }

    /* Set the thread to be fifo real time scheduled */
    if (pthread_attr_setschedpolicy(&attr, SCHED_FIFO)) {
        GST_WARNING("failed to set FIFO scheduling policy\n");
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }

    /* Set the display thread priority */
    schedParam.sched_priority = GstTIVideoThreadPriority;
    if (pthread_attr_setschedparam(&attr, &schedParam)) {
        GST_WARNING("failed to set scheduler parameters\n");
        return FALSE;
    }

    /* Create decoder thread */
    if (pthread_create(&imgdec1->decodeThread, &attr,
            gst_tiimgdec1_decode_thread, (void*)imgdec1)) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("failed to create decode thread\n"), (NULL));
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }
    gst_tithread_set_status(imgdec1, TIThread_CODEC_CREATED);

    /* Destroy the custom thread attributes */
    if (pthread_attr_destroy(&attr)) {
        GST_WARNING("failed to destroy thread attrs\n");
        gst_tiimgdec1_exit_image(imgdec1);
        return FALSE;
    }

    /* Make sure circular buffer and display buffer handles are created by
     * decoder thread.
     */
    Rendezvous_meet(imgdec1->waitOnDecodeThread);

    if (imgdec1->circBuf == NULL || imgdec1->hOutBufTab == NULL) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("decode thread failed to create circbuf or display buffer handles\n"),
        (NULL));
        return FALSE;
    }

    GST_LOG("Finish\n");
    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec1_exit_image
 *    Shut down any running image decoder, and reset the element state.
 ******************************************************************************/
static gboolean gst_tiimgdec1_exit_image(GstTIImgdec1 *imgdec1)
{
    void*    thread_ret;
    gboolean checkResult;

    GST_LOG("Begin\n");

    /* Drain the pipeline if it hasn't already been drained */
    if (!imgdec1->drainingEOS) {
       gst_tiimgdec1_drain_pipeline(imgdec1);
     }

    /* Shut down the decode thread */
    if (gst_tithread_check_status(
            imgdec1, TIThread_CODEC_CREATED, checkResult)) {
        GST_LOG("shutting down decode thread\n");

        Rendezvous_force(imgdec1->waitOnDecodeThread);
        if (pthread_join(imgdec1->decodeThread, &thread_ret) == 0) {
            if (thread_ret == GstTIThreadFailure) {
                GST_DEBUG("decode thread exited with an error condition\n");
            }
        }
    }

    /* Shut down any remaining items */
    if (imgdec1->waitOnDecodeDrain) {
        Rendezvous_delete(imgdec1->waitOnDecodeDrain);
        imgdec1->waitOnDecodeDrain = NULL;
    }

    if (imgdec1->waitOnDecodeThread) {
        Rendezvous_delete(imgdec1->waitOnDecodeThread);
        imgdec1->waitOnDecodeThread = NULL;
    }

    /* Shut down thread status management */
    imgdec1->threadStatus = 0UL;
    pthread_mutex_destroy(&imgdec1->threadStatusMutex);

    GST_LOG("Finish\n");
    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec1_change_state
 *     Manage state changes for the image stream.  The gStreamer documentation
 *     states that state changes must be handled in this manner:
 *        1) Handle ramp-up states
 *        2) Pass state change to base class
 *        3) Handle ramp-down states
 ******************************************************************************/
static GstStateChangeReturn gst_tiimgdec1_change_state(GstElement *element,
                                GstStateChange transition)
{
    GstStateChangeReturn  ret    = GST_STATE_CHANGE_SUCCESS;
    GstTIImgdec1          *imgdec1 = GST_TIIMGDEC1(element);

    GST_LOG("Begin change_state (%d)\n", transition);

    /* Handle ramp-up state changes */
    switch (transition) {
        case GST_STATE_CHANGE_NULL_TO_READY:
            break;
        default:
            break;
    }

    /* Pass state changes to base class */
    ret = GST_ELEMENT_CLASS(parent_class)->change_state(element, transition);
    if (ret == GST_STATE_CHANGE_FAILURE)
        return ret;

    /* Handle ramp-down state changes */
    switch (transition) {
        case GST_STATE_CHANGE_READY_TO_NULL:
            GST_LOG("State changed from READY to NULL.  Shutting down any"
                    "running image decoders\n");
            /* Shut down any running image decoder */
            if (!gst_tiimgdec1_exit_image(imgdec1)) {
                return GST_STATE_CHANGE_FAILURE;
            }
            break;

        default:
            break;
    }

    GST_LOG("Finish change_state\n");
    return ret;
}

/******************************************************************************
 * gst_tiimgdec1_codec_stop
 *     Stop codec engine
 *****************************************************************************/
static gboolean gst_tiimgdec1_codec_stop (GstTIImgdec1  *imgdec1)
{
    /* Shut down remaining items */
    if (imgdec1->circBuf) {
        GstTICircBuffer *circBuf;

        GST_LOG("freeing cicrular input buffer\n");

        circBuf               = imgdec1->circBuf;
        imgdec1->circBuf      = NULL;
        imgdec1->framerateNum = 0;
        imgdec1->framerateDen = 0;
        gst_ticircbuffer_unref(circBuf);
    }

    if (imgdec1->hOutBufTab) {
        GST_LOG("freeing output buffers\n");
        gst_tidmaibuftab_unref(imgdec1->hOutBufTab);
        imgdec1->hOutBufTab = NULL;
    }

    if (imgdec1->hIe) {
        GST_LOG("closing image decoder\n");
        Idec1_delete(imgdec1->hIe);
        imgdec1->hIe = NULL;
    }

    if (imgdec1->hEngine) {
        GST_LOG("closing codec engine\n");
        Engine_close(imgdec1->hEngine);
        imgdec1->hEngine = NULL;
    }

    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec1_codec_start
 *     Initialize codec engine
 *****************************************************************************/
static gboolean gst_tiimgdec1_codec_start (GstTIImgdec1  *imgdec1)
{
    BufferGfx_Attrs        gfxAttrs  = BufferGfx_Attrs_DEFAULT;

    /* Open the codec engine */
    GST_LOG("opening codec engine \"%s\"\n", imgdec1->engineName);
    imgdec1->hEngine = Engine_open((Char *) imgdec1->engineName, NULL, NULL);

    if (imgdec1->hEngine == NULL) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("failed to open codec engine \"%s\"\n", imgdec1->engineName), (NULL));
        return FALSE;
    }
    GST_LOG("opened codec engine \"%s\" successfully\n", imgdec1->engineName);

    if (!gst_tiimgdec1_set_codec_attrs(imgdec1)) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("Error while trying to set the codec attrs\n"), (NULL));
        return FALSE;
    }

    GST_LOG("opening image decoder \"%s\"\n", imgdec1->codecName);
    imgdec1->hIe = Idec1_create(imgdec1->hEngine, (Char*)imgdec1->codecName,
                      &imgdec1->params, &imgdec1->dynParams);

    if (imgdec1->hIe == NULL) {
        GST_ELEMENT_ERROR(imgdec1, STREAM, CODEC_NOT_FOUND,
        ("failed to create image decoder: %s\n", imgdec1->codecName), (NULL));
        GST_DEBUG("Verify that the values being used for input and output ColorSpace are supported by your codec.\n");
        GST_LOG("closing codec engine\n");
        return FALSE;
    }

    /* Create a circular input buffer */
    imgdec1->circBuf = gst_ticircbuffer_new(
                           Idec1_getInBufSize(imgdec1->hIe), 3, FALSE);

    if (imgdec1->circBuf == NULL) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, NO_SPACE_LEFT,
        ("failed to create circular input buffer\n"), (NULL));
        return FALSE;
    }

    /* Display buffer contents if displayBuffer=TRUE was specified */
    gst_ticircbuffer_set_display(imgdec1->circBuf, imgdec1->displayBuffer);

    /* Define the number of display buffers to allocate.  This number must be
     * at least 1, but should be more if codecs don't return a display buffer
     * after every process call.  If this has not been set via set_property(),
     * default to the value set above based on device type.
     */
    if (imgdec1->numOutputBufs == 0) {
        imgdec1->numOutputBufs = 1;
    }

    /* Create codec output buffers */
    GST_LOG("creating output buffer table\n");
    gfxAttrs.colorSpace     = gst_tiimgdec1_codec_color_space_to_dmai(imgdec1->params.forceChromaFormat);
    gfxAttrs.dim.width      = imgdec1->params.maxWidth;
    gfxAttrs.dim.height     = imgdec1->params.maxHeight;
    gfxAttrs.dim.lineLength = BufferGfx_calcLineLength(
                                  gfxAttrs.dim.width, gfxAttrs.colorSpace);

    gfxAttrs.bAttrs.memParams.align = 128;

    /* By default, new buffers are marked as in-use by the codec */
    gfxAttrs.bAttrs.useMask = gst_tidmaibuffer_CODEC_FREE;

    imgdec1->hOutBufTab = gst_tidmaibuftab_new(imgdec1->numOutputBufs,
        Idec1_getOutBufSize(imgdec1->hIe),
        BufferGfx_getBufferAttrs(&gfxAttrs));

    if (imgdec1->hOutBufTab == NULL) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, NO_SPACE_LEFT,
        ("failed to create output buffers\n"), (NULL));
        return FALSE;
    }

    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec1_decode_thread
 *     Call the image codec to process a full input buffer
 ******************************************************************************/
static void* gst_tiimgdec1_decode_thread(void *arg)
{
    GstTIImgdec1           *imgdec1        = GST_TIIMGDEC1(gst_object_ref(arg));
    GstBuffer              *encDataWindow  = NULL;
    gboolean               codecFlushed    = FALSE;
    void                   *threadRet      = GstTIThreadSuccess;
    BufferGfx_Attrs        gfxAttrs        = BufferGfx_Attrs_DEFAULT;
    Buffer_Handle          hDstBuf;
    Int32                  encDataConsumed;
    GstClockTime           encDataTime;
    GstClockTime           frameDuration;
    Buffer_Handle          hEncDataWindow;
    BufferGfx_Dimensions   dim;
    GstBuffer              *outBuf;
    Int                    bufIdx;
    Int                    ret;


    GST_LOG("Begin\n");
    /* Calculate the duration of a single frame in this stream */
    frameDuration = gst_tiimgdec1_frame_duration(imgdec1);

    /* Initialize codec engine */
    ret = gst_tiimgdec1_codec_start(imgdec1);

    /* Notify main thread that is ok to continue initialization */
    Rendezvous_meet(imgdec1->waitOnDecodeThread);
    Rendezvous_reset(imgdec1->waitOnDecodeThread);

    if (ret == FALSE) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("failed to start codec\n"), (NULL));
        goto thread_exit;
    }

    /* Main thread loop */
    while (TRUE) {

        /* Obtain an encoded data frame */
        encDataWindow  = gst_ticircbuffer_get_data(imgdec1->circBuf);
        encDataTime    = GST_BUFFER_TIMESTAMP(encDataWindow);
        hEncDataWindow = GST_TIDMAIBUFFERTRANSPORT_DMAIBUF(encDataWindow);

        /* If we received a data frame of zero size, there is no more data to
         * process -- exit the thread.  If we weren't told that we are
         * draining the pipeline, something is not right, so exit with an
         * error.
         */
        if (GST_BUFFER_SIZE(encDataWindow) == 0) {
            GST_LOG("no image data remains\n");
            if (!imgdec1->drainingEOS) {
                goto thread_failure;
            }

            goto thread_exit;
        }

        /* Obtain a free output buffer for the decoded data */
        if (!(hDstBuf = gst_tidmaibuftab_get_buf(imgdec1->hOutBufTab))) {
            GST_ELEMENT_ERROR(imgdec1, RESOURCE, READ,
                ("failed to get a free contiguous buffer from BufTab\n"), 
                (NULL));
            goto thread_failure;
        }

        /* Make sure the whole buffer is used for output */
        BufferGfx_resetDimensions(hDstBuf);

        /* Create a BufferGfx object that will point to a reference buffer from
         * The circular buffer.  This is needed for the decoder which requires
         * that the input buffer be a BufferGfx object.
         */
        BufferGfx_getBufferAttrs(&gfxAttrs)->reference = TRUE;
        imgdec1->hInBuf = Buffer_create(Idec1_getInBufSize(imgdec1->hIe),
                                BufferGfx_getBufferAttrs(&gfxAttrs));
        Buffer_setUserPtr(imgdec1->hInBuf, Buffer_getUserPtr(hEncDataWindow));
        Buffer_setNumBytesUsed(imgdec1->hInBuf, 
                               Buffer_getSize(hEncDataWindow));

        /* Set the dimensions of the buffer to the resolution */
        BufferGfx_getDimensions(hDstBuf, &dim);
        dim.width = imgdec1->params.maxWidth;
        dim.height = imgdec1->params.maxHeight;
        BufferGfx_setColorSpace(imgdec1->hInBuf,
                     ColorSpace_UYVY); 
        dim.lineLength = BufferGfx_calcLineLength(imgdec1->params.maxWidth,
                                  gfxAttrs.colorSpace);

        BufferGfx_setDimensions(hDstBuf, &dim);
        Buffer_setNumBytesUsed(hDstBuf, Buffer_getSize(hDstBuf));

        /* Invoke the image decoder */
        GST_LOG("invoking the image decoder\n");
        ret             = Idec1_process(imgdec1->hIe, imgdec1->hInBuf, hDstBuf);
        encDataConsumed = (codecFlushed) ? 0 :
                          Buffer_getNumBytesUsed(hEncDataWindow);

        /* Delete the temporary Graphics buffer used for the decode process */
        Buffer_delete(imgdec1->hInBuf);

        if (ret < 0) {
            GST_ELEMENT_ERROR(imgdec1, STREAM, DECODE, 
            ("failed to decode image buffer\n"), (NULL));
            goto thread_failure;
        }

        /* If no data was used we must have some kind of decode error */
        if (ret == Dmai_EBITERROR && encDataConsumed == 0 && !codecFlushed) {
            GST_ELEMENT_ERROR(imgdec1, STREAM, DECODE,
            ("fatal bit error\n"), (NULL));
            goto thread_failure;
        }

        if (ret > 0) {
            GST_LOG("Idec1_process returned success code %d\n", ret); 
        }

        /* Release the referdece buffer, and tell the circular buffer how much
         * data was consumed.
         */
        ret = gst_ticircbuffer_data_consumed(imgdec1->circBuf, encDataWindow,
                  encDataConsumed);
        encDataWindow = NULL;

        if (!ret) {
            goto thread_failure;
        }


        /* Set the source pad capabilities based on the decoded frame
         * properties.
         */
        gst_tiimgdec1_set_source_caps(imgdec1, hDstBuf);

        /* Create a DMAI transport buffer object to carry a DMAI buffer to
         * the source pad.  The transport buffer knows how to release the
         * buffer for re-use in this element when the source pad calls
         * gst_buffer_unref().
         */
        outBuf = gst_tidmaibuffertransport_new(hDstBuf, imgdec1->hOutBufTab);
        gst_buffer_set_data(outBuf, GST_BUFFER_DATA(outBuf),
            gst_ti_correct_display_bufSize(hDstBuf));
        gst_buffer_set_caps(outBuf, GST_PAD_CAPS(imgdec1->srcpad));

        /* If we have a valid time stamp, set it on the buffer */
        if (imgdec1->genTimeStamps &&
            GST_CLOCK_TIME_IS_VALID(encDataTime)) {
            GST_LOG("image timestamp value: %llu\n", encDataTime);
            GST_BUFFER_TIMESTAMP(outBuf) = encDataTime;
            GST_BUFFER_DURATION(outBuf)  = frameDuration;
        }
        else {
            GST_BUFFER_TIMESTAMP(outBuf) = GST_CLOCK_TIME_NONE;
        }

        /* Tell circular buffer how much time we consumed */
        gst_ticircbuffer_time_consumed(imgdec1->circBuf, frameDuration);

        /* Push the transport buffer to the source pad */
        GST_LOG("pushing display buffer to source pad\n");

        if (gst_pad_push(imgdec1->srcpad, outBuf) != GST_FLOW_OK) {
            GST_DEBUG("push to source pad failed\n");
            goto thread_failure;
        }

        /* Release buffers no longer in use by the codec */
        Buffer_freeUseMask(hDstBuf, gst_tidmaibuffer_CODEC_FREE);
    }

thread_failure:

    gst_tithread_set_status(imgdec1, TIThread_CODEC_ABORTED);
    gst_ticircbuffer_consumer_aborted(imgdec1->circBuf);
    threadRet = GstTIThreadFailure;

thread_exit:
 
    /* Re-claim any buffers owned by the codec */
    bufIdx = BufTab_getNumBufs(GST_TIDMAIBUFTAB_BUFTAB(imgdec1->hOutBufTab));

    while (bufIdx-- > 0) {
        Buffer_Handle hBuf = BufTab_getBuf(
            GST_TIDMAIBUFTAB_BUFTAB(imgdec1->hOutBufTab), bufIdx);
        Buffer_freeUseMask(hBuf, gst_tidmaibuffer_CODEC_FREE);
    }

    /* Release the last buffer we retrieved from the circular buffer */
    if (encDataWindow) {
        gst_ticircbuffer_data_consumed(imgdec1->circBuf, encDataWindow, 0);
    }

    /* We have to wait to shut down this thread until we can guarantee that
     * no more input buffers will be queued into the circular buffer
     * (we're about to delete it).  
     */
    Rendezvous_meet(imgdec1->waitOnDecodeThread);
    Rendezvous_reset(imgdec1->waitOnDecodeThread);

    /* Notify main thread that we are done draining before we shutdown the
     * codec, or we will hang.  We proceed in this order so the EOS event gets
     * propagated downstream before we attempt to shut down the codec.  The
     * codec-shutdown process will block until all BufTab buffers have been
     * released, and downstream-elements may hang on to buffers until
     * they get the EOS.
     */
    Rendezvous_force(imgdec1->waitOnDecodeDrain);

    /* Stop codec engine */
    if (gst_tiimgdec1_codec_stop(imgdec1) < 0) {
        GST_ERROR("failed to stop codec\n");
    }

    gst_object_unref(imgdec1);

    GST_LOG("Finish image decode_thread (%d)\n", (int)threadRet);
    return threadRet;
}


/******************************************************************************
 * gst_tiimgdec1_drain_pipeline
 *    Wait for the decode thread to finish processing queued input data.
 ******************************************************************************/
static void gst_tiimgdec1_drain_pipeline(GstTIImgdec1 *imgdec1)
{
    gboolean checkResult;

    /* If the decode thread hasn't been created, there is nothing to drain. */
    if (!gst_tithread_check_status(
             imgdec1, TIThread_CODEC_CREATED, checkResult)) {
        return;
    }

    imgdec1->drainingEOS = TRUE;
    gst_ticircbuffer_drain(imgdec1->circBuf, TRUE);

    /* Tell the decode thread that it is ok to shut down */
    Rendezvous_force(imgdec1->waitOnDecodeThread);

    /* Wait for the decoder to finish draining */
    Rendezvous_meet(imgdec1->waitOnDecodeDrain);
}


/******************************************************************************
 * gst_tiimgdec1_frame_duration
 *    Return the duration of a single frame in nanoseconds.
 ******************************************************************************/
static GstClockTime gst_tiimgdec1_frame_duration(GstTIImgdec1 *imgdec1)
{
    GST_LOG("Begin\n");
    /* Default to 29.97 if the frame rate was not specified */
    if (imgdec1->framerateNum == 0 && imgdec1->framerateDen == 0) {
        GST_DEBUG("framerate not specified; using 29.97fps");
        imgdec1->framerateNum = 30000;
        imgdec1->framerateDen = 1001;
    }

    GST_LOG("Finish\n");
    return (GstClockTime)
        ((1 / ((gdouble)imgdec1->framerateNum/(gdouble)imgdec1->framerateDen))
         * GST_SECOND);
}


