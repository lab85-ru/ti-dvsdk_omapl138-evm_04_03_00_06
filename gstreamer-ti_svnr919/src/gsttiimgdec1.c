/*******************************************************************************
 * gst_tiimgdec1_set_codec_attrs
 *     Set the attributes for the decode codec.  The general order is to give
 *     preferdece to values passed in by the user on the command line,
 *     then check the stream for the information.  If the required information
 *     cannot be determined by either method then error out.
 ******************************************************************************/
static gboolean gst_tiimgdec1_set_codec_attrs(GstTIImgdec1 *imgdec1)
{ 
    char *toColor;
    GST_LOG("Begin\n");

    /* Set Resolution 
     *
     * NOTE:  We assume that there is only 1 resolution.  i.e. there is
     *        no seperate value being used for input resolution and
     *        output resolution
     */
    imgdec1->params.maxWidth = 
                    imgdec1->width == 0 ?
                    imgdec1->params.maxWidth :
                    imgdec1->width;
    imgdec1->params.maxHeight =
                    imgdec1->height == 0 ?
                    imgdec1->params.maxHeight :
                    imgdec1->height;
    /* Set displayWidth to 0 in order to use decoded image width */
    imgdec1->dynParams.displayWidth = 0;

    /* Check for valid values (NOTE: minimum width and height are 64) */
    if (imgdec1->params.maxWidth < 64) {
        GST_ERROR("The resolution width (%ld) is too small.  Must be at least 64\n", imgdec1->params.maxWidth);
        return FALSE;
    }
    if (imgdec1->params.maxHeight < 64) {
        GST_ERROR("The resolution height (%ld) is too small.  Must be at least 64\n", imgdec1->params.maxHeight);
        return FALSE;
    }

    /* Make the color human readable */
    toColor = gst_tiimgdec1_codec_color_space_to_str(imgdec1->params.forceChromaFormat);

    GST_DEBUG("\nCodec Parameters:\n"
              "\tparams.maxWidth = %ld\n"
              "\tparams.maxHeight = %ld\n"
              "\tparams.forceChromaFormat = %ld (%s)\n"
              "\tparams.maxScans - %ld\n"
              "\tparams.dataEndianness = %ld\n",
              imgdec1->params.maxWidth, imgdec1->params.maxHeight,
              imgdec1->params.forceChromaFormat, toColor,
              imgdec1->params.maxScans, imgdec1->params.dataEndianness);
    GST_LOG("Finish\n");
    return TRUE;
}

