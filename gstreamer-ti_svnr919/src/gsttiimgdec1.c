/*****************************************************************************
 * gst_tiimgdec1_init_env
 *  Initialize element property default by reading environment variables.
 *****************************************************************************/
static void gst_tiimgdec1_init_env(GstTIImgdec1 *imgdec1)
{
    GST_LOG("gst_tiimgdec1_init_env - begin");

    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_engineName")) {
        imgdec1->engineName = gst_ti_env_get_string("GST_TI_TIImgdec1_engineName");
        GST_LOG("Setting engineName=%s\n", imgdec1->engineName);
    }

    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_codecName")) {
        imgdec1->codecName = gst_ti_env_get_string("GST_TI_TIImgdec1_codecName");
        GST_LOG("Setting codecName=%s\n", imgdec1->codecName);
    }
    
    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_numOutputBufs")) {
        imgdec1->numOutputBufs = 
                            gst_ti_env_get_int("GST_TI_TIImgdec1_numOutputBufs");
        GST_LOG("Setting numOutputBufs=%ld\n", imgdec1->numOutputBufs);
    }

    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_displayBuffer")) {
        imgdec1->displayBuffer = 
                gst_ti_env_get_boolean("GST_TI_TIImgdec1_displayBuffer");
        GST_LOG("Setting displayBuffer=%s\n",
                 imgdec1->displayBuffer  ? "TRUE" : "FALSE");
    }
 
    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_genTimeStamps")) {
        imgdec1->genTimeStamps = 
                gst_ti_env_get_boolean("GST_TI_TIImgdec1_genTimeStamps");
        GST_LOG("Setting genTimeStamps =%s\n", 
                    imgdec1->genTimeStamps ? "TRUE" : "FALSE");
    }
    
    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_framerate")) {
        imgdec1->framerateNum = gst_ti_env_get_int("GST_TI_TIImgdec1_framerate");
        imgdec1->framerateDen = 1;
        
        /* If 30fps was specified, use 29.97 */        
        if (imgdec1->framerateNum == 30) {
            imgdec1->framerateNum = 30000;
            imgdec1->framerateDen = 1001;
        }
    }

    if (gst_ti_env_is_defined("GST_TI_TIImgdec1_resolution")) {
        sscanf(gst_ti_env_get_string("GST_TI_TIImgdec1_resolution"), "%dx%d", 
                &imgdec1->width,&imgdec1->height);
        GST_LOG("Setting resolution=%dx%d\n", imgdec1->width, imgdec1->height);
    }
    
    GST_LOG("gst_tiimgdec_init_env - end");
}

/******************************************************************************
 * gst_tiimgdec1_init
 *    Initializes a new element instance, instantiates pads and sets the pad
 *    callback functions.
 ******************************************************************************/
static void gst_tiimgdec1_init(GstTIImgdec1 *imgdec1, GstTIImgdec1Class *gclass)
{
    GST_LOG("Begin\n");
    /* Instantiate decoded image sink pad
     *
     * Fixate on our static template caps insteam of writing a getcaps
     * function which is overkill for this element.
     */
    imgdec1->sinkpad =
        gst_pad_new_from_static_template(&sink_factory, "sink");
    gst_pad_set_setcaps_function(
        imgdec1->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec1_set_sink_caps));
//    gst_pad_set_getcaps_function(
//        imgdec1->sinkpad, GST_DEBUG_FUNCPTR(gst_pad_proxy_getcaps));
    gst_pad_set_event_function(
        imgdec1->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec1_sink_event));
    gst_pad_set_chain_function(
        imgdec1->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec1_chain));
    gst_pad_fixate_caps(imgdec1->sinkpad,
        gst_caps_make_writable(
            gst_caps_copy(gst_pad_get_pad_template_caps(imgdec1->sinkpad))));

    /* Instantiate deceoded image source pad
     *
     * Fixate on our static template caps insteam of writing a getcaps
     * function which is overkill for this element.
     */
    imgdec1->srcpad =
        gst_pad_new_from_static_template(&src_factory, "src");
//    gst_pad_set_getcaps_function(
//        imgdec1->srcpad, GST_DEBUG_FUNCPTR(gst_pad_proxy_getcaps));
    gst_pad_fixate_caps(imgdec1->srcpad,
        gst_caps_make_writable(
            gst_caps_copy(gst_pad_get_pad_template_caps(imgdec1->srcpad))));

    /* Add pads to TIImgdec1 element */
    gst_element_add_pad(GST_ELEMENT(imgdec1), imgdec1->sinkpad);
    gst_element_add_pad(GST_ELEMENT(imgdec1), imgdec1->srcpad);

    /* Initialize TIImgdec1 state */
    imgdec1->engineName         = NULL;
    imgdec1->codecName          = NULL;
    imgdec1->displayBuffer      = FALSE;
    imgdec1->genTimeStamps      = FALSE;
    imgdec1->width              = 0;
    imgdec1->height             = 0;

    imgdec1->hEngine            = NULL;
    imgdec1->hIe                = NULL;
    imgdec1->drainingEOS        = FALSE;
    imgdec1->threadStatus       = 0UL;
    imgdec1->capsSet            = FALSE;

    imgdec1->waitOnDecodeThread = NULL;
    imgdec1->waitOnDecodeDrain  = NULL;

    imgdec1->framerateNum       = 0;
    imgdec1->framerateDen       = 0;

    imgdec1->numOutputBufs      = 0UL;
    imgdec1->hOutBufTab         = NULL;
    imgdec1->circBuf            = NULL;

    gst_tiimgdec1_init_env(imgdec1);

    GST_LOG("Finish\n");
}

/******************************************************************************
 * gst_tiimgdec1_set_property
 *     Set element properties when requested.
 ******************************************************************************/
static void gst_tiimgdec1_set_property(GObject *object, guint prop_id,
                const GValue *value, GParamSpec *pspec)
{
    GstTIImgdec1 *imgdec1 = GST_TIIMGDEC1(object);

    GST_LOG("Begin\n");

    switch (prop_id) {
        case PROP_ENGINE_NAME:
            if (imgdec1->engineName) {
                g_free((gpointer)imgdec1->engineName);
            }
            imgdec1->engineName =
                (gchar*)g_malloc(strlen(g_value_get_string(value)) + 1);
            strcpy((gchar *)imgdec1->engineName, g_value_get_string(value));
            GST_LOG("setting \"engineName\" to \"%s\"\n", imgdec1->engineName);
            break;
        case PROP_CODEC_NAME:
            if (imgdec1->codecName) {
                g_free((gpointer)imgdec1->codecName);
            }
            imgdec1->codecName =
                (gchar*)g_malloc(strlen(g_value_get_string(value)) + 1);
            strcpy((gchar*)imgdec1->codecName, g_value_get_string(value));
            GST_LOG("setting \"codecName\" to \"%s\"\n", imgdec1->codecName);
            break;
        case PROP_RESOLUTION:
            sscanf(g_value_get_string(value), "%dx%d", &imgdec1->width,
                    &imgdec1->height);
            GST_LOG("setting \"resolution\" to \"%dx%d\"\n",
                imgdec1->width, imgdec1->height);
            break;
        case PROP_NUM_OUTPUT_BUFS:
            imgdec1->numOutputBufs = g_value_get_int(value);
            GST_LOG("setting \"numOutputBufs\" to \"%ld\"\n",
                imgdec1->numOutputBufs);
            break;
        case PROP_FRAMERATE:
        {
            imgdec1->framerateNum = g_value_get_int(value);
            imgdec1->framerateDen = 1;

            /* If 30fps was specified, use 29.97 */
            if (imgdec1->framerateNum == 30) {
                imgdec1->framerateNum = 30000;
                imgdec1->framerateDen = 1001;
            }

            GST_LOG("setting \"frameRate\" to \"%2.2lf\"\n",
                (gdouble)imgdec1->framerateNum /
                (gdouble)imgdec1->framerateDen);
            break;
        }
        case PROP_DISPLAY_BUFFER:
            imgdec1->displayBuffer = g_value_get_boolean(value);
            GST_LOG("setting \"displayBuffer\" to \"%s\"\n",
                imgdec1->displayBuffer ? "TRUE" : "FALSE");
            break;
        case PROP_GEN_TIMESTAMPS:
            imgdec1->genTimeStamps = g_value_get_boolean(value);
            GST_LOG("setting \"genTimeStamps\" to \"%s\"\n",
                imgdec1->genTimeStamps ? "TRUE" : "FALSE");
            break;
        default:
            G_OBJECT_WARN_INVALID_PROPERTY_ID(object, prop_id, pspec);
            break;
    }

    GST_LOG("Finish\n");
}

