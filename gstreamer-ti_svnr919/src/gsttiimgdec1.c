/*******************************************************************************
 * gst_tiimgdec1_set_sink_caps_helper
 *     This function will look at the capabilities given and set the values
 *     for the decoder if they were not specified on the command line.
 *     It returns TRUE if everything passes and FALSE if there is no
 *     capability in the buffer and the value was not specified on the
 *     command line.
 ******************************************************************************/
static gboolean gst_tiimgdec1_set_sink_caps_helper(GstPad *pad, GstCaps *caps)
{
    GstTIImgdec1 *imgdec1;
    GstStructure *capStruct;
    const gchar  *mime;
    GstTICodec   *codec = NULL;

    GST_LOG("Begin\n");

    imgdec1   = GST_TIIMGDEC1(gst_pad_get_parent(pad));

    /* Set the default values */
    imgdec1->params = Idec1_Params_DEFAULT;
    imgdec1->dynParams = Idec1_DynamicParams_DEFAULT;

    /* Set codec to JPEG Decoder */
    codec = gst_ticodec_get_codec("JPEG Image Decoder");

    /* Report if the required codec was not found */
    if (!codec) {
        GST_ELEMENT_ERROR(imgdec1, STREAM, CODEC_NOT_FOUND,
        ("unable to find codec needed for stream"), (NULL));
        return FALSE;
    }

    /* Configure the element to use the detected engine name and codec, unless
     * they have been using the set_property function.
     */
    if (!imgdec1->engineName) {
        imgdec1->engineName = codec->CE_EngineName;
    }
    if (!imgdec1->codecName) {
        imgdec1->codecName = codec->CE_CodecName;
    }

    if (!caps) {
        GST_INFO("No caps on input.  Using command line values");
        imgdec1->capsSet = TRUE;
        return TRUE;
    }

    capStruct = gst_caps_get_structure(caps, 0);
    mime      = gst_structure_get_name(capStruct);

    GST_INFO("requested sink caps:  %s", gst_caps_to_string(caps));

    /* Generic Video Properties */
    if (!strncmp(mime, "video/", 6)) {
        gint  framerateNum;
        gint  framerateDen;
        gint  width;
        gint  height;
   
        if (!imgdec1->framerateNum) {        
            if (gst_structure_get_fraction(capStruct, "framerate", 
                                &framerateNum, &framerateDen)) {
                imgdec1->framerateNum = framerateNum;
                imgdec1->framerateDen = framerateDen;
            }
        }

        /* Get the width and height of the frame if available */
        if (!imgdec1->width)
            if (gst_structure_get_int(capStruct, "width", &width)) 
                imgdec1->params.maxWidth = width;
        if (!imgdec1->height)
            if (gst_structure_get_int(capStruct, "height", &height))
                imgdec1->params.maxHeight = height;
    }

    /* Shut-down any running image decoder */
    if (!gst_tiimgdec1_exit_image(imgdec1)) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
        ("unable to shut-down running image decoder"), (NULL));
        return FALSE;
    }

    /* Flag that we have run this code at least once */
    imgdec1->capsSet = TRUE;

    GST_LOG("Finish\n");
    return TRUE;
}

/******************************************************************************
 * gst_tiimgdec1_set_sink_caps
 *     Negotiate our sink pad capabilities.
 ******************************************************************************/
static gboolean gst_tiimgdec1_set_sink_caps(GstPad *pad, GstCaps *caps)
{
    GstTIImgdec1 *imgdec1;
    imgdec1   = GST_TIIMGDEC1(gst_pad_get_parent(pad));

    GST_LOG("Begin\n");

    /* If this call fails then unref the gobject */
    if (!gst_tiimgdec1_set_sink_caps_helper(pad, caps)) {
        GST_ELEMENT_ERROR(imgdec1, STREAM, NOT_IMPLEMENTED,
        ("stream type not supported"), (NULL));
        gst_object_unref(imgdec1);
        return FALSE;
    }

    GST_LOG("sink caps negotiation successful\n");
    GST_LOG("Finish\n");
    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec1_set_source_caps
 *     Negotiate our source pad capabilities.
 ******************************************************************************/
static gboolean gst_tiimgdec1_set_source_caps(
                    GstTIImgdec1 *imgdec1, Buffer_Handle hBuf)
{
    BufferGfx_Dimensions  dim;
    GstCaps              *caps;
    gboolean              ret;
    GstPad               *pad;
    guint32              format;

    GST_LOG("Begin\n");
    pad = imgdec1->srcpad;

    /* Create a UYVY caps object using the dimensions from the given buffer */
    BufferGfx_getDimensions(hBuf, &dim);

    format = gst_tiimgdec1_codec_color_space_to_fourcc(imgdec1->params.forceChromaFormat);

    if (format == -1) {
        GST_ERROR("Could not convert codec color space to fourcc");
        return FALSE;
    }
    caps =
        gst_caps_new_simple("video/x-raw-yuv",
            "format",    GST_TYPE_FOURCC,   format,
            "framerate", GST_TYPE_FRACTION, imgdec1->framerateNum,
                                            imgdec1->framerateDen,
            "width",     G_TYPE_INT,        dim.width,
            "height",    G_TYPE_INT,        dim.height,
            NULL);

    /* Set the source pad caps */
    GST_DEBUG("setting source caps to UYVY:  %s", gst_caps_to_string(caps));
    ret = gst_pad_set_caps(pad, caps);
    gst_caps_unref(caps);

    GST_LOG("Finish\n");
    return ret;
}


/******************************************************************************
 * gst_tiimgdec1_sink_event
 *     Perform event processing on the input stream.  At the moment, this
 *     function isn't needed as this element doesn't currently perform any
 *     specialized event processing.  We'll leave it in for now in case we need
 *     it later on.
 ******************************************************************************/
static gboolean gst_tiimgdec1_sink_event(GstPad *pad, GstEvent *event)
{
    GstTIImgdec1 *imgdec1;
    gboolean      ret;

    GST_LOG("Begin\n");

    imgdec1 = GST_TIIMGDEC1(GST_OBJECT_PARENT(pad));

    GST_DEBUG("pad \"%s\" received:  %s\n", GST_PAD_NAME(pad),
        GST_EVENT_TYPE_NAME(event));

    switch (GST_EVENT_TYPE(event)) {

        case GST_EVENT_NEWSEGMENT:
            /* maybe save and/or update the current segment (e.g. for output
             * clipping) or convert the event into one in a different format
             * (e.g. BYTES to TIME) or drop it and set a flag to send a
             * newsegment event in a different format later
             */
            ret = gst_pad_push_event(imgdec1->srcpad, event);
            break;

        case GST_EVENT_EOS:
            /* end-of-stream: process any remaining decoded frame data */
            GST_LOG("no more input; draining remaining decoded image data\n");

            if (!imgdec1->drainingEOS) {
               gst_tiimgdec1_drain_pipeline(imgdec1);
             }

            /* Propagate EOS to downstream elements */
            ret = gst_pad_push_event(imgdec1->srcpad, event);
            break;

        case GST_EVENT_FLUSH_STOP:
            ret = gst_pad_push_event(imgdec1->srcpad, event);
            break;

        /* Unhandled events */
        case GST_EVENT_BUFFERSIZE:
        case GST_EVENT_CUSTOM_BOTH:
        case GST_EVENT_CUSTOM_BOTH_OOB:
        case GST_EVENT_CUSTOM_DOWNSTREAM:
        case GST_EVENT_CUSTOM_DOWNSTREAM_OOB:
        case GST_EVENT_CUSTOM_UPSTREAM:
        case GST_EVENT_FLUSH_START:
        case GST_EVENT_NAVIGATION:
        case GST_EVENT_QOS:
        case GST_EVENT_SEEK:
        case GST_EVENT_TAG:
        default:
            ret = gst_pad_event_default(pad, event);
            break;

    }

    GST_LOG("Finish\n");
    return ret;

}


/******************************************************************************
 * gst_tiimgdec1_chain
 *    This is the main processing routine.  This function receives a buffer
 *    from the sink pad, processes it, and pushes the result to the source
 *    pad.
 ******************************************************************************/
static GstFlowReturn gst_tiimgdec1_chain(GstPad * pad, GstBuffer * buf)
{
    GstTIImgdec1  *imgdec1 = GST_TIIMGDEC1(GST_OBJECT_PARENT(pad));
    GstCaps       *caps    = GST_BUFFER_CAPS(buf);
    GstFlowReturn  flow    = GST_FLOW_OK;
    gboolean       checkResult;

    /* If the decode thread aborted, signal it to let it know it's ok to
     * shut down, and communicate the failure to the pipeline.
     */
    if (gst_tithread_check_status(imgdec1, TIThread_CODEC_ABORTED,
            checkResult)) {
        flow = GST_FLOW_UNEXPECTED;
        goto exit;
    }

    /* If we have not negotiated the caps at least once then do so now */
    if (!imgdec1->capsSet) {
        if (!gst_tiimgdec1_set_sink_caps_helper(pad, caps)) {
            GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
            ("Could not set caps"), (NULL));
            flow = GST_FLOW_UNEXPECTED;
            goto exit;
        }
    }

    /* If our engine handle is currently NULL, then either this is our first
     * buffer or the upstream element has re-negotiated our capabilities which
     * resulted in our engine being closed.  In either case, we need to
     * initialize (or re-initialize) our image decoder to handle the new
     * stream.
     */
    if (imgdec1->hEngine == NULL) {
        if (!gst_tiimgdec1_init_image(imgdec1)) {
            GST_ELEMENT_ERROR(imgdec1, RESOURCE, FAILED,
            ("unable to initialize image\n"), (NULL));
            flow = GST_FLOW_UNEXPECTED;
            goto exit;
        }

        GST_TICIRCBUFFER_TIMESTAMP(imgdec1->circBuf) =
            GST_CLOCK_TIME_IS_VALID(GST_BUFFER_TIMESTAMP(buf)) ?
            GST_BUFFER_TIMESTAMP(buf) : 0ULL;
    }

    /* Queue up the encoded data stream into a circular buffer */
    if (!gst_ticircbuffer_queue_data(imgdec1->circBuf, buf)) {
        GST_ELEMENT_ERROR(imgdec1, RESOURCE, WRITE,
        ("Failed to queue input buffer into circular buffer\n"), (NULL));
        flow = GST_FLOW_UNEXPECTED;
        goto exit;
    }

exit:
    gst_buffer_unref(buf);
    return flow;
}

